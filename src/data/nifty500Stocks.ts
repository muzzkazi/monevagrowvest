// Generate random RSI between 30-70 with some variance
const generateRSI = (symbol: string): number => {
  const hash = symbol.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0);
  return 30 + (hash % 40) + Math.floor(Math.random() * 10);
};

// Generate 52-week high/low based on typical price patterns
const generate52WeekRange = (marketCap: number, sector: string): { high: number; low: number } => {
  // Estimate base price from market cap (simplified)
  const basePrice = Math.max(50, Math.min(5000, marketCap / 100));
  const volatility = sector === "IT" || sector === "Pharma" ? 0.35 : 
                     sector === "Banking" || sector === "NBFC" ? 0.3 : 
                     sector === "Metals" || sector === "Energy" ? 0.4 : 0.25;
  
  return {
    high: Math.round(basePrice * (1 + volatility)),
    low: Math.round(basePrice * (1 - volatility * 0.8))
  };
};

// Build complete stock database with estimated fundamentals
export const nifty500Stocks: StockInfo[] = nifty500Raw.map(stock => {
  const defaults = sectorDefaults[stock.sector] || sectorDefaults["Diversified"];
  const range = generate52WeekRange(stock.marketCap, stock.sector);
  
  // Add some variance to default values
  const variance = () => 0.8 + Math.random() * 0.4; // 80% to 120%
  
  return {
    symbol: stock.symbol,
    name: stock.name,
    sector: stock.sector,
    marketCap: stock.marketCap,
    peRatio: Math.round((defaults.peRatio || 20) * variance() * 10) / 10,
    pbRatio: Math.round((defaults.pbRatio || 3) * variance() * 10) / 10,
    eps: Math.round(stock.marketCap / 1000 * variance() * 10) / 10, // Simplified EPS estimate
    dividendYield: Math.round((defaults.dividendYield || 1) * variance() * 10) / 10,
    debtToEquity: Math.round((defaults.debtToEquity || 0.5) * variance() * 100) / 100,
    roe: Math.round((defaults.roe || 15) * variance() * 10) / 10,
    rsi: generateRSI(stock.symbol),
    high52Week: range.high,
    low52Week: range.low,
  };
});

